  init() {
    const stored = localStorage.getItem('mop_preview_lang');
    const sys = (navigator.language || '').toLowerCase().startsWith('zh') ? 'zh' : 'en';
    this.lang = stored === 'en' || stored === 'zh' ? stored : sys;
    return this.lang;
  },
  t(key) {
    return this.strings[this.lang]?.[key] ?? this.strings.zh[key] ?? key;
  },
  setLang(lang) {
    if (this.strings[lang]) { this.lang = lang; localStorage.setItem('mop_preview_lang', lang); }
  },
  getLang() { return this.lang; },
